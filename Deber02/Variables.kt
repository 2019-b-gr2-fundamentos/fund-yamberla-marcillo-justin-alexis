fun main(args: Array<String>){
    val numero = 14
    println(numero)
    val apellido = "Yamberla"
    println(apellido)
    val myfloat = 34.43F
    println(myfloat)
    val mydouble = 21.666454
    println(mydouble)
    val myBinary = 0b010101
    println(myBinary)
    val myLong = 40L
    println(myLong)
    val myTrueBoolean = true
    println(myTrueBoolean)
    val myFalseBoolean = false
    println(myFalseBoolean)
    val caracter = 'a'
    println(caracter)
}

